package encapsulation;

public class Student {
	
	private String name ;
	private int age ;

	//Set and get name
	public void setName(String name) {
		this.name = name;
	}
	
    public String getName() {
		return name;
	}
  
  //Set and get age
    public void setAge(int age) {
		this.age = age;
	}
	
    public int getAge() {
		return age;
	}
    
    
  
	
	
}
